/* WAP to ask for FIVE NUMBERS from user and display them in a SINGLE LINE seperated by comma */
   
#include<stdio.h>
void main()
{
	int a,b,c,d,e;
	
	printf("Enter the values of FIVE NUMBERS :");
	scanf("%d%d%d%d%d",&a,&b,&c,&d,&e);
	
	printf("Presentation of FIVE NUMBER in a SINGLE LINE :%d,%d,%d,%d,%d",a,b,c,d,e);
}
